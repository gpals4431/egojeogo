const sizeClasses = {
  sm: "text-[4px] leading-[4px]",
  md: "text-[6px] leading-[6px]",
  lg: "text-[8px] leading-[8px]",
};

export function PixelIcon({ type, className, size = "md" }: PixelIconProps) {
  const iconData = icons[type];
  
  return (
    <pre 
      className={cn(
        "font-mono select-none text-current whitespace-pre",
        sizeClasses[size],
        className
      )}
      aria-hidden="true"
    >
      {iconData.join("\n")}
    </pre>
  );
}

